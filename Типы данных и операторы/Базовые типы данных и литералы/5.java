public class Main {

    public static void main(String[] args) {
        // Пример 1: int и double - double
        int a1 = 10;
        double b1 = 5.5;
        double result1 = a1 + b1;
        System.out.println("rezult (int + double): " + result1);

        // Пример 2: byte и int - int
        byte a2 = 50;
        int b2 = 100;
        int result2 = a2 * b2;
        System.out.println("rezult (byte * int): " + result2);
    }
}
